"""
Feature Engineering Module

This module creates additional customer behavior features
to improve churn prediction performance.
"""


def create_customer_value(df):
    """
    Creates customer value feature.

    Customer_Value = Total Purchase / Years with company
    """

    df["Customer_Value"] = (
        df["Total_Purchase"] /
        df["Years"]
    )

    return df



def engagement_category(sites):
    """
    Creates customer engagement groups.
    """

    if sites <= 5:
        return "Low"

    elif sites <= 10:
        return "Medium"

    else:
        return "High"



def age_group(age):
    """
    Creates customer age groups.
    """

    if age < 35:
        return "Young"

    elif age < 55:
        return "Middle"

    else:
        return "Senior"



def create_features(df):
    """
    Apply all feature engineering steps.
    """

    df = create_customer_value(df)

    df["Engagement_Level"] = (
        df["Num_Sites"]
        .apply(engagement_category)
    )


    df["Age_Group"] = (
        df["Age"]
        .apply(age_group)
    )


    return df